DROP TABLE IF EXISTS games;
CREATE TABLE games (
    GAME_DATE_EST VARCHAR(10),
    GAME_ID BIGINT,
    GAME_STATUS_TEXT VARCHAR(255),
    HOME_TEAM_ID BIGINT,
    VISITOR_TEAM_ID BIGINT,
    SEASON INT,
    TEAM_ID_home BIGINT,
    PTS_home INT,
    FG_PCT_home DECIMAL(5, 4),
    FT_PCT_home DECIMAL(5, 4),
    FG3_PCT_home DECIMAL(5, 4),
    AST_home INT,
    REB_home INT,
    TEAM_ID_away BIGINT,
    PTS_away INT,
    FG_PCT_away DECIMAL(5, 4),
    FT_PCT_away DECIMAL(5, 4),
    FG3_PCT_away DECIMAL(5, 4),
    AST_away INT,
    REB_away INT,
    HOME_TEAM_WINS BOOLEAN
);

