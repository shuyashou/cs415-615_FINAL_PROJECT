    FG3_PCT DECIMAL(5, 3),
    FTM INT,
    FTA INT,
    FT_PCT DECIMAL(5, 3),
    OREB INT,
    DREB INT,
    REB INT,
    AST INT,
    STL INT,
    BLK INT,
    TOVR INT,
    PF INT,
    PTS INT,
