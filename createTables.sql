    MAX_YEAR INT,
    ABBREVIATION VARCHAR(10),
    NICKNAME VARCHAR(50),
    YEARFOUNDED INT,
    CITY VARCHAR(50),
    ARENA VARCHAR(100),
    ARENACAPACITY INT,
    TOWNER VARCHAR(100),
    GENERALMANAGER VARCHAR(100),
    HEADCOACH VARCHAR(100),
    DLEAGUEAFFILIATION VARCHAR(100)
